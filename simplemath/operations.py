def add(a, b):
    """Adds two numbers.

    Args:
        a (int, float): First number.
        b (int, float): Second number.

    Returns:
        int, float: The sum of `a` and `b`.
    """
    return a + b


def subtract(a, b):
    """Subtracts one number from another.

    Args:
        a (int, float): First number.
        b (int, float): Second number.

    Returns:
        int, float: The result of `a - b`.
    """
    return a - b
